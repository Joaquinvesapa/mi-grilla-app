// Mirrors the `groups` and `group_members` tables in Supabase.

import type { Profile } from "./profile-types";

// в”Ђв”Ђ Roles в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

const GROUP_ROLE = {
  ADMIN: "admin",
  MEMBER: "member",
} as const;

type GroupRole = (typeof GROUP_ROLE)[keyof typeof GROUP_ROLE];

export { GROUP_ROLE };
export type { GroupRole };

// в”Ђв”Ђ DB rows в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

export interface Group {
  id: string;
  name: string;
  invite_code: string;
  created_by: string;
  created_at: string;
}

export interface GroupMember {
  id: string;
  group_id: string;
  user_id: string;
  role: GroupRole;
  joined_at: string;
}

// в”Ђв”Ђ Enriched types for UI в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

export interface GroupMemberWithProfile extends GroupMember {
  profile: Profile;
}

/** Group with member count вЂ” used in the groups list */
export interface GroupWithMeta extends Group {
  member_count: number;
  my_role: GroupRole;
}

/** Full group detail with members */
export interface GroupDetail extends Group {
  members: GroupMemberWithProfile[];
  my_role: GroupRole;
}

/** Result from find_group_by_invite_code RPC */
export interface GroupPreview {
  id: string;
  name: string;
  invite_code: string;
  member_count: number;
}
